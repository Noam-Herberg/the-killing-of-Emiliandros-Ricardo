function paragraphHtml(lines) {
  return lines.map((line) => `<p>${line}</p>`).join("");
}

function contactsHtml(contacts = []) {
  return contacts
    .map(
      (contact) => `
        <article class="contact-card">
          <h3>${contact.name}</h3>
          <p>${contact.description}</p>
        </article>
      `
    )
    .join("");
}

function portraitHtml(character, large = false) {
  const initials = character.name
    .split(" ")
    .filter((word) => word !== "Dossier")
    .map((word) => word[0])
    .join("")
    .slice(0, 2);

  if (!character.image) {
    return `<div class="portrait-placeholder ${large ? "large" : ""}"><span>${initials || "?"}</span></div>`;
  }

  return `<img src="${character.image}" alt="${character.name}" onerror="this.replaceWith(createPortraitFallback('${initials || "?"}', ${large}))">`;
}

