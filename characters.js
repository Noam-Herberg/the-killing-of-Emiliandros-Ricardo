function paragraphHtml(lines) {
  return lines.map((line) => `<p>${line}</p>`).join("");
}

function portraitHtml(character, large = false) {
  const initials = character.name
    .split(" ")
    .filter((word) => word !== "Dossier")
    .map((word) => word[0])
    .join("")
    .slice(0, 2);

  if (!character.image) {
    return `<div class="portrait-placeholder ${large ? "large" : ""}"><span>${initials || "?"}</span></div>`;
  }

  return `<img src="${character.image}" alt="${character.name}" onerror="this.replaceWith(createPortraitFallback('${initials || "?"}', ${large}))">`;
}

function createPortraitFallback(initials, large) {
  const fallback = document.createElement("div");
  fallback.className = `portrait-placeholder ${large ? "large" : ""}`;
  fallback.innerHTML = `<span>${initials}</span>`;
  return fallback;
}

function renderSelectionPage() {
  const grid = document.querySelector("#characterGrid");
  const bioName = document.querySelector("#bioName");
  const bioText = document.querySelector("#bioText");
  const selectButton = document.querySelector("#selectButton");
  const confirmOverlay = document.querySelector("#confirmOverlay");
  const cancelConfirm = document.querySelector("#cancelConfirm");
  const proceedConfirm = document.querySelector("#proceedConfirm");
  let selectedCharacterUrl = "";

  if (!grid || !bioName || !bioText || !selectButton) {
    return;
  }

  grid.innerHTML = characters
    .map(
      (character, index) => `
        <button class="character-card" type="button" data-character-id="${character.id}">
          <div class="portrait-frame">${portraitHtml(character)}</div>
          <span class="character-name">${character.name}</span>
          <span class="character-role">${character.role}</span>
          <span class="slot-number">0${index + 1}</span>
        </button>
      `
    )
    .join("");

  grid.addEventListener("click", (event) => {
    const card = event.target.closest(".character-card");
    if (!card) {
      return;
    }

    const character = characters.find((item) => item.id === card.dataset.characterId);
    if (!character) {
      return;
    }

    document.querySelectorAll(".character-card").forEach((item) => item.classList.remove("selected"));
    card.classList.add("selected");
    bioName.textContent = character.name;
    bioText.innerHTML = paragraphHtml(character.publicBackground);
    selectedCharacterUrl = `character.html?id=${encodeURIComponent(character.id)}`;
    selectButton.href = selectedCharacterUrl;
    selectButton.classList.remove("disabled");
    selectButton.removeAttribute("aria-disabled");
  });

  if (!confirmOverlay || !cancelConfirm || !proceedConfirm) {
    return;
  }

  selectButton.addEventListener("click", (event) => {
    if (!selectedCharacterUrl) {
      return;
    }

    event.preventDefault();
    confirmOverlay.hidden = false;
    cancelConfirm.focus();
  });

  cancelConfirm.addEventListener("click", () => {
    confirmOverlay.hidden = true;
    selectButton.focus();
  });

  proceedConfirm.addEventListener("click", () => {
    if (selectedCharacterUrl) {
      window.location.href = selectedCharacterUrl;
    }
  });

  confirmOverlay.addEventListener("click", (event) => {
    if (event.target === confirmOverlay) {
      confirmOverlay.hidden = true;
      selectButton.focus();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && !confirmOverlay.hidden) {
      confirmOverlay.hidden = true;
      selectButton.focus();
    }
  });
}

function renderDetailPage() {
  const detailPage = document.querySelector("#detailPage");
  const detailName = document.querySelector("#detailName");
  const detailPortrait = document.querySelector("#detailPortrait");
  const detailPublic = document.querySelector("#detailPublic");
  const detailSecret = document.querySelector("#detailSecret");

  if (!detailPage || !detailName || !detailPortrait || !detailPublic || !detailSecret) {
    return;
  }

  const params = new URLSearchParams(window.location.search);
  const character = characters.find((item) => item.id === params.get("id")) || characters[0];

  detailName.textContent = character.name;
  detailPortrait.innerHTML = portraitHtml(character, true);
  detailPublic.innerHTML = paragraphHtml(character.publicBackground);
  detailSecret.innerHTML = paragraphHtml(character.secretBackground);
}

renderSelectionPage();
renderDetailPage();
